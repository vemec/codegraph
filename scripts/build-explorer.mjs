// Bundles the Graph Explorer client (Sigma + graphology inlined) into a single
// minified IIFE and writes it as a TS string module that `src/outputs/html.ts`
// embeds inline — so the generated graph.html is fully offline, no CDN.
//
// Runs before build/test/dev (see package.json pre-scripts). The output file is
// generated and gitignored.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

// eslint-disable-next-line import/no-extraneous-dependencies
import esbuild from 'esbuild';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const out = path.join(root, 'src', 'outputs', 'explorer-client.generated.ts');

const result = await esbuild.build({
  entryPoints: [path.join(root, 'src', 'explorer', 'client.ts')],
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: 'es2020',
  minify: true,
  write: false,
  legalComments: 'none',
});

const code = result.outputFiles[0].text;
const banner =
  '/* eslint-disable prettier/prettier, no-template-curly-in-string */\n// AUTO-GENERATED by scripts/build-explorer.mjs — do not edit, gitignored.\n';

fs.writeFileSync(
  out,
  `${banner}export const EXPLORER_CLIENT = ${JSON.stringify(code)};\n`,
);
process.stderr.write(
  `explorer client bundled: ${(code.length / 1024).toFixed(0)} KB\n`,
);
